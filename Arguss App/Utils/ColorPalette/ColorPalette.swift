import UIKit

extension UIColor {
    @nonobjc class var mainDarkBlue: UIColor {
        return UIColor(red: 6.0/255.0, green: 22.0/255.0, blue: 52.0/255.0, alpha: 1.0)
    }
    
    @nonobjc class var mainLightBlue: UIColor {
        return UIColor(red: 22.0/255.0, green: 127.0/255.0, blue: 176.0/255.0, alpha: 1.0)
    }
    
}
